#ljust  es para mover el string a la izuqierda
#rjust  es para mover el string a la derecha
#center  es para mover el strin al centro

nombre = "Jose David Lozada"
print(nombre.ljust(30), ".") #30 es los espacios que le quiero dar a la derecha
print(nombre.rjust(30), ".") #20 es los espacios que le quiero dar a la izquierda
print(nombre.center(40), ".") #le do 20espacios a la derecha y 20 a la izquierda